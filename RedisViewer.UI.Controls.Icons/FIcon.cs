            DependencyProperty.Register("Icon", typeof(FIconEnum), typeof(FIcon), new FrameworkPropertyMetadata(FIconEnum.None, FrameworkPropertyMetadataOptions.AffectsRender));

        public FIconEnum Icon
        {
            get { return (FIconEnum)GetValue(IconProperty); }
            set { SetValue(IconProperty, value); }
        }

        public double Size
        {
            get { return TextElement.GetFontSize(this); }
            set { TextElement.SetFontSize(this, value); }
        }

        public Brush Foreground
        {
            get => TextElement.GetForeground(this);
            set => TextElement.SetForeground(this, value);
        }

        protected override Size MeasureOverride(Size availableSize)
            => new Size(Math.Min(availableSize.Width, Size), Math.Min(availableSize.Height, Size));

        protected override void OnRender(DrawingContext drawingContext)
        {
            if (Icon != FIconEnum.None)
            {
                var ft = new FormattedText(
                    char.ConvertFromUtf32((int)Icon),
                    CultureInfo.InvariantCulture,
                    FlowDirection.LeftToRight,
                    _iconTypeface[Icon],
                    Size,
                    Foreground,
                    null,
                    TextFormattingMode.Ideal,
                    VisualTreeHelper.GetDpi(this).PixelsPerDip);

                drawingContext.DrawText(ft, new Point());
            }

            base.OnRender(drawingContext);
        }
